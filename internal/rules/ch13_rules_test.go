package rules

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func writeStructuralFile(t *testing.T, dir, name, content string) string {
	t.Helper()
	path := filepath.Join(dir, name)
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		t.Fatalf("write %s: %v", name, err)
	}
	return path
}

func bodyWithNLines(n int) string {
	if n <= 0 {
		return ""
	}
	var builder strings.Builder
	for i := 0; i < n; i++ {
		builder.WriteString("\t_ = 0\n")
	}
	return builder.String()
}

func TestLIM01_FunctionLineLimits(t *testing.T) {
	tests := []struct {
		name      string
		source    string
		wantCount int
	}{
		{
			name:      "long func decl fails",
			source:    "package sample\nfunc f() {\n" + bodyWithNLines(51) + "}\n",
			wantCount: 1,
		},
		{
			name:      "long func literal fails independently",
			source:    "package sample\nfunc f() {\n\tg := func() {\n" + bodyWithNLines(51) + "\t}\n\t_ = g\n}\n",
			wantCount: 1,
		},
		{
			name:      "exactly 50 lines passes",
			source:    "package sample\nfunc f() {\n" + bodyWithNLines(50) + "}\n",
			wantCount: 0,
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			dir := t.TempDir()
			file := writeStructuralFile(t, dir, "sample.go", tc.source)

			diags, err := NewLIM01().Run(Context{Files: []string{file}})
			if err != nil {
				t.Fatalf("run LIM-01: %v", err)
			}
			if len(diags) != tc.wantCount {
				t.Fatalf("expected %d diagnostics, got %d", tc.wantCount, len(diags))
			}
		})
	}
}

